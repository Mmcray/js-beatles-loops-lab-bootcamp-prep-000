function theBeatlesPlay(musicians, instruments) {
  var beatles = [];
  var i;
  var mus = musicians.length;
  for (i = 0; i < mus; i++) {
  beatles.push(musicians[i] + " plays " + instruments[i]);
  }
  return beatles;
}
